#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*s;
	int		a;

	a = 0;
	while (str[a] != '\0')
		a++;
	s = malloc(sizeof(char) * a + 1);
	if (s == 0)
		return (0);
	while (a != -1)
	{
		s[a] = str[a];
		a--;
	}
	return (s);
}
